&nbsp;« Journal Extime » est un web film poétique qui dresse un portrait de la ville à travers le regard d’une jeune adolescente Molly Bloom Penelope qui parcourt la ville et rêve de son marin parti à la pêche en Islande.<br />
<br />
&nbsp;Forgé par Michel Tournier, le « journal extime » sonde l’intimité non pas de l’auteur mais du territoire qui lui est extérieur. Le « journal extime » est un mouvement centrifuge de conquête et de découverte, une écriture du dehors. Entre les archives et le monde d'aujourd'hui, l'histoire et la géographie, Molly Bloom fait résonner les espaces de la ville entre eux et dialogue sur la toile en faisant signe vers le monde. Ce web film crée des liens entre la ville, des sons, des musiques, des personnages qu'elle rencontre.
